public class One {
    public static void main(String[] args) {
        int x = 10;
        int a = 22;
        System.out.println(x / a);
        System.out.println(a / x);

        float z = 10;
        int c = 20;
        System.out.println(c / (int) z);
        System.out.println((int) z / c);

        float w = (int) 10;
        float d = (int) 22;
        System.out.println(d / w);
    }
}
